public class CarAttribute {

    private String personId;
    private String pRegionId;
    private String carId;
    private String carNum;
    private String carCate;
    private String carBrand;


    public String getPersonId() {
        return personId;
    }

    public void setPersonId(String personId) {
        this.personId = personId;
    }

    public String getpRegionId() {
        return pRegionId;
    }

    public void setpRegionId(String pRegionId) {
        this.pRegionId = pRegionId;
    }

    public String getCarId() {
        return carId;
    }

    public void setCarId(String carId) {
        this.carId = carId;
    }

    public String getCarNum() {
        return carNum;
    }

    public void setCarNum(String carNum) {
        this.carNum = carNum;
    }

    public String getCarCate() {
        return carCate;
    }

    public void setCarCate(String carCate) {
        this.carCate = carCate;
    }

    public String getCarBrand() {
        return carBrand;
    }

    public void setCarBrand(String carBrand) {
        this.carBrand = carBrand;
    }

}
